package com.example.cat_caring.ui.activity;

public class donation {

    private String name;
    private int imageId;

    public donation(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;

    }


}
